package OperatorsTypes;

public class ArithmeticOperators {

    public static void main(String[] args) {

        int price = 50000;
        int quantity = 2;

        System.out.println("Addition: " + (price + quantity));
        System.out.println("Subtraction: " + (price - quantity));
        System.out.println("Multiplication: " + (price * quantity));
        System.out.println("Division: " + (price / quantity));
        System.out.println("Modulus: " + (price % quantity));
    }
}
