package OperatorsTypes;

public class RelationalOperators {

    public static void main(String[] args) {

        int age = 20;

        System.out.println(age == 20);
        System.out.println(age != 18);
        System.out.println(age > 18);
        System.out.println(age < 25);
        System.out.println(age >= 18);
        System.out.println(age <= 30);
    }
}
